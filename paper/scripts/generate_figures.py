#!/usr/bin/env python3
"""Generate portable LaTeX figure fragments from constitutional-review reports."""

from __future__ import annotations

import csv
from pathlib import Path


ROOT = Path(__file__).resolve().parents[2]
REPORT_DIR = ROOT / "reports"
FIGURE_DIR = ROOT / "paper" / "figures"
BASELINE_CSV = REPORT_DIR / "constitutional-review-campaign-v0.csv"
PAIRED_CSV = REPORT_DIR / "constitutional-review-paired-import-v1.csv"

