def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="") as handle:
        return list(csv.DictReader(handle))


def baseline_rows() -> list[dict[str, str]]:
    return [row for row in read_rows(BASELINE_CSV) if row["caseKey"] == "baseline"]


def row_float(row: dict[str, str], field: str) -> float:
    return float(row[field])


def coord(value: float, lower: float, upper: float, start: float, length: float) -> float:
    if upper == lower:
        return start + (length / 2.0)
    return start + ((value - lower) / (upper - lower)) * length


def write(name: str, content: str) -> None:
    FIGURE_DIR.mkdir(parents=True, exist_ok=True)
    (FIGURE_DIR / name).write_text(content, encoding="utf-8")


def fmt(value: float) -> str:
    return f"{value:.1f}"


